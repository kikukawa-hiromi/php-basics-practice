<?php
///////////////////////////////////////
// 割引計算プログラム
//
// ・元の価格: 5000円
// ・割引率: 20%
// ・割引後の価格を計算して表示
///////////////////////////////////////

// 定義
$original_price = 5000;
$discount_rate = 0.20;

// 割引後の価格を計算
$discount_amount = $original_price * $discount_rate;
$final_price = $original_price - $discount_amount;

// 表示
echo "<b>割引計算プログラム</b><br>";
echo "元の価格: " . number_format($original_price) . "円<br>";
echo "割引率: " . $discount_rate * 100 . "%<br>";
echo "割引後の価格: " . number_format($final_price) . "円<br>";

///////////////////////////////////////
// 偶数・奇数判定プログラム
//
// ・変数$numberに任意の整数を代入
// ・偶数なら「○○は偶数です」、奇数なら「○○は奇数です」と表示
///////////////////////////////////////

// 定義
$number = 7;

// 表示
echo "<b>偶数・奇数判定プログラム</b><br>";
if ($number % 2 == 0){
    echo "{$number}は偶数です<br>";
}else{
    echo "{$number}は奇数です<br>";
}

///////////////////////////////////////
// 複数条件の判定プログラム
//
// ・年齢が18歳以上 かつ 会員である場合: 「割引が適用されます」
// ・年齢が65歳以上 または 学生である場合: 「シニア・学生割引が適用されます」
///////////////////////////////////////

// 定義
$age = 25;
$is_member = true;
$is_student = false;

// 表示
echo "<b>複数条件の判定プログラム</b><br>";
// 条件1：18歳以上かつ会員
if ($age >= 18 && $is_member){
    echo "割引が適用されます<br>";
}
if ($age >= 65 && $is_student){
    echo "シニア・学生割引が適用されます<br>";
}

///////////////////////////////////////
// 複合代入演算子の練習
//
// ・初期スコア: 100点
// ・ボーナスステージクリア: +50点
// ・ダメージを受ける: -30点
// ・スコア　2倍アイテム使用: ×2
// ・最終スコアを表示
///////////////////////////////////////

// 定義
$score = 100;

// 表示
echo "<b>複合代入演算子の練習</b><br>";
echo "初期スコア：{$score}点<br>";
$score += 50;
echo "ボーナス後：{$score}点<br>";
$score -= 30;
echo "ダメージ後：{$score}点<br>";
$score *= 2;
echo "最終スコア：{$score}点<br>";